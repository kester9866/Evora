async def import_all():
    db = await aiosqlite.connect(DATABASE_PATH)
    db.row_factory = aiosqlite.Row

    # Recreate tables with fresh schema
    await db.execute("DROP TABLE IF EXISTS knowledge_chunks")
    await db.execute("DROP TABLE IF EXISTS bridges")
    await db.executescript("""
        CREATE TABLE bridges (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name_zh TEXT NOT NULL,
            name_en TEXT,
            dynasty TEXT,
            type TEXT,
            material TEXT,
            province TEXT,
            city TEXT,
            district TEXT,
            coordinates TEXT,
            year_built TEXT,
            length_m REAL,
            width_m REAL,
            span_m REAL,
            description TEXT,
            has_model INTEGER DEFAULT 0,
            model_url TEXT,
            image_url TEXT,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        );
        CREATE TABLE knowledge_chunks (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            bridge_id INTEGER,
            text TEXT NOT NULL,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            FOREIGN KEY (bridge_id) REFERENCES bridges(id) ON DELETE SET NULL
        );
    """)
    await db.commit()

    print(f"Inserting {len(BRIDGES)} bridges...")
    bridge_ids = {}
    for i, b in enumerate(BRIDGES, 1):
        cursor = await db.execute("""
            INSERT INTO bridges (name_zh, name_en, dynasty, type, material, province, city, district,
                coordinates, year_built, length_m, width_m, span_m, description, has_model, model_url)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (
            b["name_zh"], b.get("name_en"), b["dynasty"], b["type"], b.get("material"),
            b["province"], b.get("city"), b.get("district"), b.get("coordinates"), b.get("year_built"),
            b.get("length_m"), b.get("width_m"), b.get("span_m"), b.get("description"),
            b.get("has_model", 0), b.get("model_url")
        ))
        bridge_ids[b["name_zh"]] = cursor.lastrowid
        if i % 20 == 0:
            print(f"  {i}/{len(BRIDGES)} bridges inserted...")

    await db.commit()
    print(f"Done. {len(BRIDGES)} bridges inserted.")

    print(f"\nInserting {len(KNOWLEDGE_CHUNKS)} knowledge chunks...")
    for i, (text, bridge_name) in enumerate(KNOWLEDGE_CHUNKS, 1):
        bridge_id = bridge_ids.get(bridge_name) if bridge_name else None
        await db.execute(
            "INSERT INTO knowledge_chunks (bridge_id, text) VALUES (?, ?)",
            [bridge_id, text]
        )
        if i % 50 == 0:
            print(f"  {i}/{len(KNOWLEDGE_CHUNKS)} chunks inserted...")

    await db.commit()
    await db.close()
    print(f"Done. {len(KNOWLEDGE_CHUNKS)} knowledge chunks inserted.")
    print(f"\nImport complete: {len(BRIDGES)} bridges, {len(KNOWLEDGE_CHUNKS)} knowledge chunks.")


if __name__ == "__main__":
    asyncio.run(import_all())
